<?php

namespace App\Util;

/**
 * Calculator util.
 *
 * @author Vladimir Kuprienko <vldmr.kuprienko@gmail.com>
 */
class Calculator
{
    /**
     * Add two numbers.
     *
     * @param float $a
     * @param float $b
     *
     * @return float
     */
    public function add($a, $b)
    {
        return $a + $b;
    }
}
